# 1. Utilizando a função "print", verifique se dois números são iguais utilizando o operador de igualdade
# ("==").

print(3 == 3)

# 2. Utilizando a função "print", verifique se dois números são diferentes utilizando o operador de
# diferença ("!=").

print(3 != 3)

# 3. Utilizando a função "print", verifique se um número é maior que outro utilizando o operador de
# maior que (">").

print(3 > 4)

# 4. Utilizando a função "print", verifique se um número é menor que outro utilizando o operador de
# menor que ("<").

print(3 < 4)

# 5. Utilizando a função "print", verifique se um número é maior ou igual a outro utilizando o operador
# de maior ou igual a (">=").

print(3 >= 4)

# 6. Utilizando a função "print", verifique se um número é menor ou igual a outro utilizando o operador
# de menor ou igual a ("<=").

print(3 <= 4)

# 7. Utilizando a função "print", verifique se duas condições são verdadeiras utilizando o operador
# lógico AND ("and").

print(10 > 0 and 10 > 100)

# 8. Utilizando a função "print", verifique se pelo menos uma das condições é verdadeira utilizando o
# operador lógico OR ("or").

print(10 > 0 or 10 < 9)

# 9. Utilizando a função "print", negue uma variável booleana utilizando o operador lógico NOT ("not").

comparacao = 10 > 100
print(not (comparacao))

# 10. Utilizando a função "print", verifique se um número está dentro de um determinado intervalo,
# utilizando os operadores lógicos AND e os operadores de comparação (">=" e "<=").

print(3 >= 2 and 3 <= 5)

# 11. Utilizando a função "print", verifique se um número está fora de um determinado intervalo,
# utilizando os operadores lógicos OR e os operadores de comparação ("<" e ">").

print(7 > 2 or 7 < 5)

# 12. Utilizando a função "print", verifique se uma string está vazia. Utilize o operador de igualdade ("==")
# e a função "len" para verificar se o tamanho da string é igual a zero.

print(len("string") == 0)

# 13. Utilizando a função "print". Verifique se uma variável é do tipo booleano. Utilize a função "type"
# para obter o tipo da variável e o operador de igualdade ("==") para comparar se o tipo de dado é
# "bool".

print(type(comparacao) == bool)
